$(document).ready(function () {
  $('.gg').click(function () {

    $('.garry_kasparov').toggle()

  })
  $('.tr').click(function () {

    $('.ii').toggle()

  })

  $('.pj').mouseenter(function () {
    $('.bro').show()
  })
  $('.pj').mouseleave(function () {
    $('.bro').hide()
  })
  $('.pu').mouseenter(function () {
    $('.qro').show()
  })
  $('.pu').mouseleave(function () {
    $('.qro').hide()
  })

 
});



